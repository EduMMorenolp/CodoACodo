// Switch

let opcion = prompt("¿Qué quiere comer?")

switch (opcion) {
   case 'papas fritas':
      console.log('Las papas fritas cuestan $ 500');
      break;
   case 'milanesa':
      console.log('La milanesa cuesta $ 2000');
      break;
   case 'ensalada':
      console.log('La ensalada cuesta $ 800');
      break;
   default:
      console.log('No tenemos esa opción');
}


